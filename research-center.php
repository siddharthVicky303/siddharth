 <h1 class="h1_Research_center">The School has also set up two independent research Centres</h1>    
 <p class="p1_Research_center">1) The J P Narayan National Centre of Excellence in the Humanities.   2) DST Centre for Policy Research.</p> 
 <p class="p1_Research_center"> The J P Narayan National Centre of Excellence in the Humanities was established in October 2022 at IIT Indore by the Ministry of Education to initiate <br>research, academics and outreach activities focusing on two areas: </p> 
 <p class="p1_Research_center">1) Digital Humanities
    2) Environmental Humanities  </p> 
    <p class="p1_Research_center">
<em>The Centre’s objective is therefore to bring together researchers in language, literature, history, publishing, computational linguistics, Archiving, sustainability<br>
studies, environment to name a few to facilitate a conversation across disciplinary divides that are now increasingly porous borders. Collectively, the goal of <br>
this diverse group of researchers is to further Humanities scholarship, create new forms of knowledge and explore technology’s impact on humanities-<br>
based research. The Centre leads and members are cognizant of and embody the changing landscape of research in the humanities and technology- a<br>
recognition that we are in a scholarship ecosystem today that is publicly visible in ways that we are unaccustomed to, a scholarship and pedagogy that are <br>
bound up with infrastructure in ways that are deeper and more explicit than we are generally accustomed to, a scholarship and pedagogy that are <br>
collaborative and depend on networks of people across disciplinary borders. The Centre will therefore function as a nodal place that fosters collaborative <br>
initiatives at the intersections of language, literature and technology and will model the highest standards of digital scholarship. In doing so, the Centre will<br> 
offer scholars a unique environment for extending the boundaries of traditional research using digital technologies. Apart from developing the above three<br> 
research areas, the JPNCOEH has a core objective of becoming a platform for undertaking long and short-term training programmes, workshops, <br>
deliberative panels and exploratory studies in the core and emerging areas in the Humanities and Multidisciplinary research and pedagogy in line with the<br>
objectives of the New Education Policy (NEP) of the Government of India.
    </p> </em>
    <p class="p1_Research_center"> 
       <em>  &ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&ensp;The J P Narayan Centre will fund innovative projects in the above research areas and will also run such projects<br>
        from the Centre. The Centre will have Research Associates, Post Doctoral Fellows and Faculty members who will be leading and developing such initiatives.<br>
        In the next one year, the Centre will also initiate academic programmes, a MS + PhD in Digital and Environmental Humanities. The expert workshops and <br>
       deliberations on the framework of these programmes are already underway. The Centre envisages policy papers, book publications and other research <br>
       publications through the activities of the Centre.        
     </p> </em>
     <p class="p1_Research_center">
      <em>DST Centre for Policy Research: IIT Indore submitted a proposal for a DST Centre for Policy Research in response to a call for setting up a Centre of <br>
      Excellence. DST has now sanctioned a Centre for Policy Research that has a team from different departments of the Institute; however, the two major <br>
      themes of the Centre involve Faculty from School of HSS and Civil Engineering. The two major themes that the Centre will work on are:</em>    
     </p> 
     <p class="p1_Research_center">
      •S&T for sectoral development, Clean Energy, Health, Agriculture, Water, Environment, and Climate Change<br>
      •Intellectual Property Ecosystem and other regulatory issues
     </p>   
     <p class="p1_Research_center">
      The Centre aims to contribute to the Science, Technology and Innovation landscape by interventions at the Technology front and at the policy and <br>
Intellectual Property front. This will be only one of such a Centre in Central India and therefore will have a special emphasis on the states of Madhya Pradesh<br>
 and Chattisgarh. The Centre will evaluate STI policy interventions designed to incentivise sustainable environmental practices related to water and <br>
climate change. A major part of the Centre will focus on Intellectual Property and patent landscape analysis to understand gaps in policy and strategy. The <br>
Centre’s objective is to undertake patent landscape analysis in specific technology areas such as Robotics, IoT, Artificial Intelligence (AI), Machine Learning<br> 
(ML)and thereby recommend policy changes or revisions as to benefit innovators and inventions in India. At the same time, the Centre will also support and <br>
develop projects to create repositories of indigenous knowledge systems and create digital databases to protect their IPR. In time, the Centre hopes to<br>
develop a digitised interface for IPR related data and provide easy access through technology agnostic interfaces. The Centre will also facilitate academic <br>
programmes and ensure that training and teaching of IP and Patent Systems are integrated to the course work of the students at IIT Indore. Apart from that,<br>
 the Centre will also hold such training sessions for students, professionals and inventors for raising awareness about the IP landscape. This will naturally <br>
include authoring and publishing White papers and policy briefs to assist policy makers to make research based, informed decisions.<br>

&ensp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;&emsp;&ensp;In short, this Centre will be a nodal policy hub for conversations and discussions about IP related<br>
 challenges and steps needed to strengthen the meaning, scope and expanse of the Intellectual Property, Patent Analysis and its links to policy making in <br>
these areas at the national level.
     </p>
